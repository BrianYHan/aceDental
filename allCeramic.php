<p>
By hand layering the porcelain, Ace Dental Laboratory certified and dedicated Ceramic artists create veneers of dynamic beauty and pearl-like translucency. Feldspathic veneers can be made as thin as 0.3 mm to 0.5 mm which requires far less tooth reduction than other veneering alternatives. These remarkable veneers are indicated for slight contour and color changes, and are available in bleach shades. Feldspathic veneers have a big impact on your patient's appearance by providing a vivid, natural, and lustrous smile!</p>


